def count_in_list(lst, item):
    """
    Count the number of occurrences of a specified item in a list.

    Parameters:
    lst (list): The list in which to count occurrences.
    item: The item to be counted in the list.

    Returns:
    int: The number of times 'item' appears in the list 'lst'.
    """
    return lst.count(item)


